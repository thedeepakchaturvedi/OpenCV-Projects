import cv2
import numpy as np

# func to give value to track bar
def empty(a):
    pass


path = 'Resources/toycar.jpg'
# creating track bar to get min max hsv value of color to grab it

cv2.namedWindow("TrackBars") # created window
cv2.resizeWindow("TrackBars",640,240)

cv2.createTrackbar("Hue Min","TrackBars",0,179,empty)
cv2.createTrackbar("Hue Max","TrackBars",179,179,empty)
cv2.createTrackbar("Sat Min","TrackBars",176,255,empty) # value input on the basis of trackbar
cv2.createTrackbar("Sat Max","TrackBars",255,255,empty)
cv2.createTrackbar("Val Min","TrackBars",0,255,empty)
cv2.createTrackbar("Val Max","TrackBars",255,255,empty)

while True:
    img=cv2.imread(path)
    print(img.shape)
    resizedImg = cv2.resize(img,(375,375))
    # converting img into HSV
    HSVimg = cv2.cvtColor(resizedImg,cv2.COLOR_BGR2HSV)

    h_min=cv2.getTrackbarPos("Hue Min","TrackBars")
    h_max=cv2.getTrackbarPos("Hue Max","TrackBars")
    s_min=cv2.getTrackbarPos("Sat Min","TrackBars")
    s_max=cv2.getTrackbarPos("Sat Max","TrackBars")
    v_min=cv2.getTrackbarPos("Val Min","TrackBars")
    v_max=cv2.getTrackbarPos("Val Max","TrackBars")
    print(h_min,h_max,s_min,s_max,v_min,v_max)
    lower = np.array([h_min,s_min,v_min])
    upper = np.array([h_max,s_max,v_max])
    mask = cv2.inRange(resizedImg,lower,upper)
    imgResult = cv2.bitwise_and(resizedImg,resizedImg,mask=mask) # add each pixel of two img and display result

